NSString *Get_todaytime_to_string() {

  //获取系统当前的时间戳
  NSDate *dat = [NSDate dateWithTimeIntervalSinceNow:0];
  NSTimeInterval a = [dat timeIntervalSince1970] * 1000;
  NSString *start_time = [NSString stringWithFormat:@"%llu", (long long)a];

  return start_time;
}
