#import "FPBankListTableViewCell.h"

@implementation FPBankListTableViewCell

- (id)initWithStyle:(UITableViewCellStyle)style
    reuseIdentifier:(NSString *)reuseIdentifier {
  self = [super initWithStyle:style reuseIdentifier:reuseIdentifier];
  if (self) {
    _bankImageView =
        [[UIImageView alloc] initWithFrame:CGRectMake(25, 12, 27, 25)];
    [self.contentView addSubview:_bankImageView];

    _bankNameLabel =
        [[UILabel alloc] initWithFrame:CGRectMake(66, 15, 200, 17)];
    [self.contentView addSubview:_bankNameLabel];
  }
  return self;
}
- (void)setSelected:(BOOL)selected animated:(BOOL)animated {
  [super setSelected:selected animated:animated];
}

@end
